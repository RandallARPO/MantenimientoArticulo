    /**
     * Método que imprime en pdf un articulo.
     *
     */
    private void btn_ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ImprimirActionPerformed
        if (articuloDevuelto.getID() != 0) {
            StringBuilder texto = new StringBuilder();
            String upceanisbn = articuloDevuelto.getUpceanisbn();
            String categoria = articuloDevuelto.getCategoria();
            String costo = "" + articuloDevuelto.getCosto();
            String impuesto1 = "" + articuloDevuelto.getImpuesto_1();
            String cantidad = "" + articuloDevuelto.getCantidad();
            String localizacion = articuloDevuelto.getLocalizacion();
            String descripcion = articuloDevuelto.getDescripcion();
            String nombre = articuloDevuelto.getNombre();
            String proveedor = articuloDevuelto.getProveedor();
            String precio = "" + articuloDevuelto.getPrecio_unitario();
            String impuesto2 = "" + articuloDevuelto.getImpuesto_2();
            String reordenamiento = "" + articuloDevuelto.getReordenamiento();
            String descripcionalterna = articuloDevuelto.getDescripcion_alterna();
            String numeroserie = articuloDevuelto.getNumero_serie();
            if (upceanisbn == null) {
                upceanisbn = "No tiene dato ingresado";
            }
            if (impuesto1 == null) {
                impuesto1 = "No tiene dato ingresado";
            }
            if (localizacion == null) {
                localizacion = "No tiene dato ingresado";
            }
            if (descripcion == null) {
                descripcion = "No tiene dato ingresado";
            }
            if (impuesto2 == null) {
                impuesto2 = "No tiene dato ingresado";
            }
            if (descripcionalterna == null) {
                descripcionalterna = "No tiene dato ingresado";
            }
            if (numeroserie == null) {
                numeroserie = "No tiene dato ingresado";
            }
            texto.append("Articulo  \n" + "UPC/EAN/ISBN: " + upceanisbn + "\n Categoría: " + categoria + "\n Costo: " + costo + "\n Impuesto 1: " + impuesto1 + "\n Cantidad: " + cantidad + "\n Localización: " + localizacion + "\n Descripción: " + descripcion + "\n Nombre del Artículo: " + nombre + "\n Proveedor: " + proveedor + "\n Precio Unitario: " + precio + "\n Impuesto 2: " + impuesto2 + "\n Nivel de Reordenamiento: " + reordenamiento + "\n Descripción Alterna: " + descripcionalterna + "\n Número de Serie: " + numeroserie);
            if (texto.length() != 0) {
                imprimir.convertirPDF(texto);
                interfmesj.mostrarMensajeExito("La impresion PDF a sido creada \n.");
            }
        } else {
            interfmesj.mostrarMensajeAdvertencia("Primero debe buscar el artículo antes de imprimirlo");
        }
    }//GEN-LAST:event_btn_ImprimirActionPerformed


