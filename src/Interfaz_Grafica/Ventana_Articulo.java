    /**
     * Método que guarda en la base de datos un nuevo articulo.
     *
     */
    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        if (txt_Categoria.getText().trim().equals("") || txt_Costo.getText().trim().equals("") || txt_Cantidad.getText().trim().equals("") || txt_Nombre.getText().trim().equals("") || txt_Proveedor.getText().trim().equals("") || txt_Precio.getText().trim().equals("") || txt_Reordenamiento.getText().trim().equals("")) {

            interfmesj.mostrarMensajeError("Ingrese todos los datos que se solicitan como obligatorios  \n están en color rojo.");
        } else {
            Articulo articulo = new Articulo();
            articulo.setUpceanisbn(txt_Upc.getText());
            articulo.setCategoria(txt_Categoria.getText());
            articulo.setCosto(Double.parseDouble(txt_Costo.getText()));
            if (!txt_Impuesto.getText().equals("")) {
                articulo.setImpuesto_1(Float.parseFloat(txt_Impuesto.getText()));
            }
            articulo.setCantidad(Integer.parseInt(txt_Cantidad.getText()));
            articulo.setLocalizacion(txt_Localizacion.getText());
            articulo.setDescripcion(txa_Descripcion.getText());
            articulo.setNombre(txt_Nombre.getText());
            articulo.setProveedor(txt_Proveedor.getText());
            articulo.setPrecio_unitario(Double.parseDouble(txt_Precio.getText()));
            if (!txt_Impuesto2.getText().equals("")) {
                articulo.setImpuesto_2(Float.parseFloat(txt_Impuesto2.getText()));
            }
            articulo.setReordenamiento(Integer.parseInt(txt_Reordenamiento.getText()));
            articulo.setDescripcion_alterna(txt_Alterna.getText());
            articulo.setNumero_serie(txt_Serie.getText());
            logica.insertarArticulo(articulo);

        }
    }//GEN-LAST:event_btn_GuardarActionPerformed

