    /**
     * Método que busca en la base de datos un articulo.
     *
     */
    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        if (!txt_Categoria.getText().trim().equals("") && !txt_Nombre.getText().trim().equals("")) {
            Articulo articulo = new Articulo();
            articulo.setCategoria(txt_Categoria.getText());
            articulo.setNombre(txt_Nombre.getText());
            articuloDevuelto = logica.buscarArticulo(articulo);
            boolean seguir = logica.continuar();
            if (seguir) {
                txt_Upc.setText(articuloDevuelto.getUpceanisbn());
                txt_Costo.setText("" + articuloDevuelto.getCosto());
                txt_Impuesto.setText("" + articuloDevuelto.getImpuesto_1());
                txt_Cantidad.setText("" + articuloDevuelto.getCantidad());
                txt_Localizacion.setText(articuloDevuelto.getLocalizacion());
                txa_Descripcion.setText(articuloDevuelto.getDescripcion());
                txt_Proveedor.setText(articuloDevuelto.getProveedor());
                txt_Precio.setText("" + articuloDevuelto.getPrecio_unitario());
                txt_Impuesto2.setText("" + articuloDevuelto.getImpuesto_2());
                txt_Reordenamiento.setText("" + articuloDevuelto.getReordenamiento());
                txt_Alterna.setText(articuloDevuelto.getDescripcion_alterna());
                txt_Serie.setText(articuloDevuelto.getNumero_serie());
                if (articuloDevuelto.getDescripcion_alterna() != null) {
                    jcb_Descripcion.setSelected(true);
                } else {
                    jcb_Descripcion.setSelected(false);
                }
                if (articuloDevuelto.getNumero_serie() != null) {
                    jcb_Serie.setSelected(true);
                } else {
                    jcb_Serie.setSelected(false);
                }
            }
        } else {
            interfmesj.mostrarMensajeAdvertencia(" La búsqueda se realizará, con los campos de nombre y categoría, \n Por favor asegúrese de llenar ambos campos");
        }

    }//GEN-LAST:event_btn_BuscarActionPerformed

